
public class TestSwitch {

	public static void main(String[] args) {
		int number=50;
		switch(number)
		{
		case 10:
		System.out.println("10");
		break;
		default:
			System.out.println("exit");
			break;
		case 20:
		System.out.println("20");
		break;
		case 50:
		System.out.println("50");
		
		
		
		
		}

	}

}
